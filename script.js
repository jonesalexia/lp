document.getElementsByClassName("column")[0].onmouseover = function () {
    document.getElementsByClassName("card-button")[0].style.background = "#2D9CDB";
    document.getElementsByClassName("card-button")[0].style.color = "#FFFFFF";
}

document.getElementsByClassName("column")[0].onmouseout = function () {
    document.getElementsByClassName("card-button")[0].style.background = "#E0E0E0";
    document.getElementsByClassName("card-button")[0].style.color = "#828282";
}

